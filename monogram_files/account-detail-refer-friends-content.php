      $('.subscribe_newsletter').on('click', function(e){
        var subscribe_email = $('#subscribe_email').val();

        jQuery('.newsletter_ajax_loader').show();

        var request = $.ajax({
            url: "http://www.monogramtea.com/discovertea/index/subscribe/",
            method: "POST",
            data: { subscribe_email : subscribe_email },
            dataType: "html"
        });
         
        request.done(function( msg ) {
          var message = JSON.parse(msg);
          console.log(message);
          jQuery('.newsletter_ajax_loader').hide();
          if(message.error_messages)
            jQuery('span.ajax_msg').html(message.error_messages).show().delay(5000).fadeOut();              
            else
            jQuery('span.ajax_msg').html('Successfully subscribed to newsletter').show().delay(5000).fadeOut();
        });
      });
